"""
plan_waypoints_bicycle_from_costmap.py

Generate risk-planned ego future waypoints from BEV cost maps using a
kinematic bicycle model.

This version supports temporal multi-frame scoring: future costmaps are warped
back into the current ego-local frame so candidate rollouts can be evaluated
against surrounding actors' future true motion.

Important distinction:
    - reference_route: a geometric guide path, usually from measurements['route']
      and lateral-offset variants.
    - risk_planned_waypoints: time-indexed ego future positions generated by
      kinematic bicycle rollout. These are NOT direct resampling of the route.

Expected route directory:
    route_dir/
        measurements/
            0000.json.gz
        costmap/
            0000.npy
        bev_meta/
            0000.json.gz        # optional

Output:
    route_dir/
        risk_waypoints_bicycle/
            0000.json.gz
        risk_waypoints_bicycle_debug/
            0000_rollouts.png
        risk_waypoints_bicycle_rgb_debug/
            0000_rgb_waypoints.jpg  # optional, with future vehicle tracks if --save_rgb_debug

Coordinate convention used by this script:
    Ego-local coordinates:
        x: forward
        y: right
        yaw = 0 means heading along +x
        positive yaw / positive steer means turning to the right (+y)

Pixel conversion:
        col = ego_center_x + y / meters_per_pixel
        row = ego_center_y - x / meters_per_pixel

Example, single route:
python tools_bev/plan_waypoints_bicycle_from_costmap.py \
  --input /path/to/route_dir \
  --save_debug \
  --verbose

Example, recursive:
python tools_bev/plan_waypoints_bicycle_from_costmap.py \
  --input /path/to/data_root \
  --recursive \
  --save_debug \
  --verbose

Example, one frame:
python tools_bev/plan_waypoints_bicycle_from_costmap.py \
  --input /path/to/route_dir \
  --frame 0001 \
  --save_debug \
  --verbose
"""

