def local_points_to_pixels(points: np.ndarray, ego_center: List[float], meters_per_pixel: float) -> np.ndarray:
    cx, cy = float(ego_center[0]), float(ego_center[1])
    points = np.asarray(points, dtype=np.float32)
    x = points[:, 0]
    y = points[:, 1]

    col = cx + y / meters_per_pixel
    row = cy - x / meters_per_pixel
    return np.stack([col, row], axis=1).astype(np.float32)


def sample_cost_bilinear(
    cost: np.ndarray,
    pixels: np.ndarray,
    out_of_bounds_cost: float,
) -> Tuple[np.ndarray, np.ndarray]:
    h, w = cost.shape
    col = pixels[:, 0]
    row = pixels[:, 1]

    valid = (col >= 0.0) & (col <= w - 1.0) & (row >= 0.0) & (row <= h - 1.0)
    sampled = np.full((len(pixels),), float(out_of_bounds_cost), dtype=np.float32)

    if not np.any(valid):
        return sampled, valid

    c = col[valid]
    r = row[valid]

    c0 = np.floor(c).astype(np.int32)
    r0 = np.floor(r).astype(np.int32)
    c1 = np.clip(c0 + 1, 0, w - 1)
    r1 = np.clip(r0 + 1, 0, h - 1)

    dc = c - c0
    dr = r - r0

    v00 = cost[r0, c0]
    v01 = cost[r0, c1]
    v10 = cost[r1, c0]
    v11 = cost[r1, c1]

    v0 = v00 * (1.0 - dc) + v01 * dc
    v1 = v10 * (1.0 - dc) + v11 * dc
    v = v0 * (1.0 - dr) + v1 * dr

    sampled[valid] = v.astype(np.float32)
    return sampled, valid


def build_vehicle_footprint_points(
    xy: np.ndarray,
    yaw: np.ndarray,
    args,
) -> np.ndarray:
    """
    Return sampled footprint points for every state.

    Shape: [N * K, 2]
    """
    xy = np.asarray(xy, dtype=np.float32)
    yaw = np.asarray(yaw, dtype=np.float32)

    if not args.score_footprint:
        return xy

    ex = float(args.ego_extent_x_m)
    ey = float(args.ego_extent_y_m)

    local_samples = np.asarray([
        [0.0, 0.0],
        [ex, 0.0],
        [-ex, 0.0],
        [0.0, ey],
        [0.0, -ey],
        [ex, ey],
        [ex, -ey],
        [-ex, ey],
        [-ex, -ey],
    ], dtype=np.float32)

    pts = []
    for p, th in zip(xy, yaw):
        c = math.cos(float(th))
        s = math.sin(float(th))
        for q in local_samples:
            # vehicle to world, x forward, y right
            x_w = p[0] + c * q[0] - s * q[1]
            y_w = p[1] + s * q[0] + c * q[1]
            pts.append([x_w, y_w])

    return np.asarray(pts, dtype=np.float32)


def build_vehicle_footprint_points_single(
    xy: np.ndarray,
    yaw: float,
    args,
) -> np.ndarray:
    """
    Return footprint sample points for one rollout state in current-frame
    ego-local coordinates.
    """
    xy = np.asarray(xy, dtype=np.float32).reshape(2)

    if not args.score_footprint:
        return xy.reshape(1, 2).astype(np.float32)

    ex = float(args.ego_extent_x_m)
    ey = float(args.ego_extent_y_m)

    local_samples = np.asarray([
        [0.0, 0.0],
        [ex, 0.0],
        [-ex, 0.0],
        [0.0, ey],
        [0.0, -ey],
        [ex, ey],
        [ex, -ey],
        [-ex, ey],
        [-ex, -ey],
    ], dtype=np.float32)

    c = math.cos(float(yaw))
    s = math.sin(float(yaw))

    pts = np.zeros_like(local_samples, dtype=np.float32)
    pts[:, 0] = xy[0] + c * local_samples[:, 0] - s * local_samples[:, 1]
    pts[:, 1] = xy[1] + s * local_samples[:, 0] + c * local_samples[:, 1]
    return pts.astype(np.float32)


def get_pose_global_xy_yaw(measurement: Dict) -> Optional[Tuple[np.ndarray, float]]:
    """
    Extract ego global XY and yaw from a measurement file.

    The current script already uses measurement['pos_global'] and
    measurement['theta'] for expert future waypoints. This helper keeps the
    same convention for temporal costmap warping.
    """
    if "pos_global" not in measurement or "theta" not in measurement:
        return None

    pos = np.asarray(measurement["pos_global"][:2], dtype=np.float32)
    yaw = float(measurement["theta"])
    if pos.shape[0] != 2 or not np.isfinite(pos).all() or not np.isfinite(yaw):
        return None
    return pos, yaw


def current_local_grid_for_costmap(
    cost_shape: Tuple[int, int],
    ego_center: List[float],
    meters_per_pixel: float,
) -> Tuple[np.ndarray, np.ndarray]:
    """
    Build current ego-local x/y arrays for every pixel of a target costmap.

    Pixel convention:
        col = ego_center_x + y / meters_per_pixel
        row = ego_center_y - x / meters_per_pixel
    Therefore:
        x = (ego_center_y - row) * meters_per_pixel
        y = (col - ego_center_x) * meters_per_pixel
    """
    h, w = cost_shape
    rows, cols = np.indices((h, w), dtype=np.float32)
    cx, cy = float(ego_center[0]), float(ego_center[1])

    x_local = (cy - rows) * float(meters_per_pixel)
    y_local = (cols - cx) * float(meters_per_pixel)
    return x_local.astype(np.float32), y_local.astype(np.float32)


def transform_current_local_grid_to_source_local(
    x_current: np.ndarray,
    y_current: np.ndarray,
    current_measurement: Dict,
    source_measurement: Dict,
) -> Optional[Tuple[np.ndarray, np.ndarray]]:
    """
    Transform a grid of points from the current ego-local frame to a source
    frame ego-local coordinate system.

    This is the key step that makes future costmaps usable for current-frame
    planning. A future costmap is normally centered/aligned with the future
    expert ego pose. We warp it back into the current ego frame before scoring
    candidate rollouts.
    """
    current_pose = get_pose_global_xy_yaw(current_measurement)
    source_pose = get_pose_global_xy_yaw(source_measurement)
    if current_pose is None or source_pose is None:
        return None

    current_pos, current_yaw = current_pose
    source_pos, source_yaw = source_pose

    c0 = math.cos(float(current_yaw))
    s0 = math.sin(float(current_yaw))

    # Current local -> global.
    global_x = current_pos[0] + x_current * c0 - y_current * s0
    global_y = current_pos[1] + x_current * s0 + y_current * c0

    # Global -> source local.
    dx = global_x - source_pos[0]
    dy = global_y - source_pos[1]

    c1 = math.cos(float(source_yaw))
    s1 = math.sin(float(source_yaw))

    x_source = dx * c1 + dy * s1
    y_source = -dx * s1 + dy * c1
    return x_source.astype(np.float32), y_source.astype(np.float32)


def warp_costmap_to_current_frame(
    source_costmap: np.ndarray,
    source_meta: Dict,
    source_measurement: Dict,
    current_measurement: Dict,
    target_shape: Tuple[int, int],
    target_ego_center: List[float],
    target_meters_per_pixel: float,
    args,
) -> Optional[np.ndarray]:
    """
    Warp a source-frame costmap into the current frame's ego-local grid.

    source_costmap is usually a future frame. Its actor positions are true
    future positions, but its coordinate system is the future expert ego frame.
    The output has the same pixel grid as the current costmap, so candidate
    rollouts generated in the current ego-local frame can be sampled directly.
    """
    # 读取未来帧的mmp和自车在costmap中的位置
    source_mpp = get_meters_per_pixel(source_meta, args.default_pixels_per_meter)
    source_ego_center = get_ego_center(source_meta, source_costmap.shape)

    # 构建当前帧每个像素对应的ego-local坐标 坐标系为x像前 y向右
    x_current, y_current = current_local_grid_for_costmap(
        cost_shape=target_shape,
        ego_center=target_ego_center,
        meters_per_pixel=target_meters_per_pixel,
    )

    # 把当前帧 local grid 转到未来帧 local grid
    transformed = transform_current_local_grid_to_source_local(
        x_current=x_current,
        y_current=y_current,
        current_measurement=current_measurement,
        source_measurement=source_measurement,
    )
    if transformed is None:
        return None

    x_source, y_source = transformed
    src_cx, src_cy = float(source_ego_center[0]), float(source_ego_center[1])

    map_x = src_cx + y_source / float(source_mpp)
    map_y = src_cy - x_source / float(source_mpp)

    warped = cv2.remap(
        source_costmap.astype(np.float32),  # 源图:未来帧costmap
        map_x.astype(np.float32),  # 每个像素应该在源图的哪个位置采样
        map_y.astype(np.float32),  # 每个像素应该在源图的哪个位置采样
        interpolation=cv2.INTER_LINEAR,  # 双线性插值
        borderMode=cv2.BORDER_CONSTANT,
        borderValue=float(args.future_warp_border_cost),
    )
    return warped.astype(np.float32)


def combine_current_and_future_costmap(
    current_costmap: np.ndarray,
    future_costmap_in_current_frame: np.ndarray,
    args,
) -> np.ndarray:
    """
    Combine current-frame costmap with a warped future-frame costmap.

    Default is max because it preserves the current static cost field while
    allowing future actor risk to appear at its future true position. Additive
    fusion is available but can over-count static road/background cost if the
    future costmap is not actor-only.
    """
    # 未来 costmap 先乘以一个权重 如果想要削弱未来风险 可以设置为0.5 想强调未来风险可以设置为2.0 原样使用未来风险就设置为1.0
    future = future_costmap_in_current_frame.astype(np.float32) * float(args.future_costmap_weight)
    current = current_costmap.astype(np.float32)

    mode = str(args.future_costmap_combine).lower()
    if mode == "max":  # 最合理的模式 逐像素取最大值
        combined = np.maximum(current, future)
    elif mode == "add":
        combined = current + future
    elif mode == "future_only":
        combined = future
    else:
        raise ValueError(f"Unsupported future_costmap_combine: {args.future_costmap_combine}")

    if float(args.temporal_cost_clip_max) > 0.0:
        combined = np.clip(combined, 0.0, float(args.temporal_cost_clip_max))

    return combined.astype(np.float32)


def costmap_to_debug_bgr(
    cost: np.ndarray,
    title: str = "",
    ego_center: Optional[List[float]] = None,
    vmax: float = 0.0,
) -> np.ndarray:
    """
    Convert a 2D costmap to a colored BGR debug image.

    This function is only used for visualization. It does not change planning.
    """
    cost = np.asarray(cost, dtype=np.float32)
    finite = np.isfinite(cost)

    if not np.any(finite):
        gray = np.zeros(cost.shape, dtype=np.uint8)
    else:
        vals = cost[finite]

        if float(vmax) <= 0.0:
            v_max = float(np.percentile(vals, 99.5))
            if v_max <= 1e-6:
                v_max = float(np.max(vals))
            if v_max <= 1e-6:
                v_max = 1.0
        else:
            v_max = float(vmax)

        norm = np.clip(cost / v_max, 0.0, 1.0)
        gray = (norm * 255.0).astype(np.uint8)

    img = cv2.applyColorMap(gray, cv2.COLORMAP_JET)

    if ego_center is not None:
        cx = int(round(float(ego_center[0])))
        cy = int(round(float(ego_center[1])))
        cv2.circle(img, (cx, cy), radius=3, color=(255, 255, 255), thickness=-1)
        cv2.circle(img, (cx, cy), radius=6, color=(0, 0, 0), thickness=1)

    if title:
        cv2.rectangle(img, (0, 0), (img.shape[1], 24), (0, 0, 0), thickness=-1)
        cv2.putText(
            img,
            title,
            (5, 17),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.45,
            (255, 255, 255),
            1,
            cv2.LINE_AA,
        )

    return img


def save_cumulative_temporal_costmap_debug(
    route_dir: Path,
    frame_name: str,
    cumulative_costmap: np.ndarray,
    current_ego_center: List[float],
    fused_future_frames: List[str],
    args,
) -> None:
    """
    Save one cumulative temporal costmap for the current frame.

    This debug map is:
        current costmap + all successfully warped future costmaps

    It is only for visualization/debug. It is not used by score_rollout().
    """
    debug_dir = route_dir / args.temporal_debug_folder
    debug_dir.mkdir(parents=True, exist_ok=True)

    num_future = len(fused_future_frames)
    title = f"{frame_name}: current + {num_future} warped future costmaps"

    img = costmap_to_debug_bgr(
        cumulative_costmap,
        title=title,
        ego_center=current_ego_center,
        vmax=float(args.temporal_debug_vmax),
    )

    out_png = debug_dir / f"{frame_name}_cumulative_temporal_costmap.png"
    cv2.imwrite(str(out_png), img)

    if args.temporal_debug_save_npy:
        out_npy = debug_dir / f"{frame_name}_cumulative_temporal_costmap.npy"
        np.save(str(out_npy), cumulative_costmap.astype(np.float32))

    out_txt = debug_dir / f"{frame_name}_cumulative_temporal_costmap_frames.txt"
    with open(out_txt, "w", encoding="utf-8") as f:
        f.write(f"current_frame: {frame_name}\n")
        f.write(f"num_fused_future_frames: {num_future}\n")
        f.write("fused_future_frames:\n")
        for name in fused_future_frames:
            f.write(f"  {name}\n")

def offset_frame_name(frame_name: str, offset: int, stride: int = 1) -> Optional[str]:
    try:
        frame_int = int(frame_name)
    except ValueError:
        return None
    width = len(frame_name)
    return str(frame_int + int(offset) * int(stride)).zfill(width)


def append_missing_temporal_map(
    temporal_costmaps: List[np.ndarray],
    current_costmap: np.ndarray,
    args,
) -> np.ndarray:
    policy = str(args.future_missing_policy).lower()
    if policy == "repeat_last" and len(temporal_costmaps) > 0:
        return temporal_costmaps[-1].copy()
    if policy == "current":
        return current_costmap.copy()
    if policy == "zero":
        return np.zeros_like(current_costmap, dtype=np.float32)
    raise ValueError(f"Unsupported future_missing_policy: {args.future_missing_policy}")


def build_temporal_costmap_bundle(
    route_dir: Path,    # 当前route的根目录，包含costmaps、measurements、meta等子目录
    frame_name: str,    # 当前帧编号,例如"0023" 根据frame_name和offset计算未来帧编号
    current_measurement: Dict,  # 当前帧的measurement的内容
    current_costmap: np.ndarray,# 当前帧的costmap数据 二维数组,[H,W]
    current_meta: Dict,  # 当前帧的meta信息
    current_ego_center: List[float],  # 当前costmap中自车中心的像素坐标,一般为[256/2, 256/2]
    current_meters_per_pixel: float,  # 当前 costmap 每个像素对应多少米,一般为0.2
    args,
) -> Dict:
    """
    Build a time-indexed list of costmaps for rollout scoring.

    Index 0 is the current costmap. Index k>0 is frame+k costmap warped into
    the current frame. This lets the planner evaluate candidate ego positions
    against surrounding vehicles' future true positions.
    """
    #################### 初始化 temporal bundle ####################
    temporal_costmaps: List[np.ndarray] = [current_costmap.astype(np.float32)]
    temporal_frames: List[str] = [frame_name]
    temporal_valid: List[bool] = [True]  # 记录每个 temporal costmap 是否有效，当前帧的costmap默认有效
    temporal_warped: List[bool] = [False]  # 记录这张 costmap 是否经过了 warp, 当前帧的cost_map本来就是在当前帧坐标系下的，所以不算warp
    missing_reasons: List[str] = []  # 记录未来帧costmap缺失的原因，方便后续分析

    # Cumulative temporal debug map:
    # current costmap + all successfully warped future costmaps.
    # This is only for debug visualization, not for scoring.
    cumulative_temporal_costmap = current_costmap.astype(np.float32).copy()
    fused_future_frames: List[str] = []

    # 如果选择禁用未来帧costmap(也就是只使用当前帧costmap,这意味着后续score_rollout只能使用当前帧costmap评分),直接返回当前帧的costmap和相关信息，未来帧部分标记为无效且未启用
    # 做消融实验时会用到这个选项，看看完全不使用未来帧costmap的效果
    if args.disable_future_costmaps:
        return {
            "costmaps": temporal_costmaps,
            "frames": temporal_frames,
            "valid": temporal_valid,
            "warped": temporal_warped,
            "missing_reasons": missing_reasons,
            "enabled": False,
        }

    # 选择 costmap 和 meta 文件夹，如果使用增强数据则选择增强数据的文件夹，否则选择原始数据的文件夹
    costmap_folder = args.costmap_folder_augmented if args.augmented else args.costmap_folder
    meta_folder = args.bev_meta_folder_augmented if args.augmented else args.bev_meta_folder

    # 遍历未来帧
    for offset in range(1, int(args.num_future_waypoints) + 1):
        future_name = offset_frame_name(frame_name, offset, stride=args.future_frame_stride)
        if future_name is None:
            missing_reasons.append(f"offset={offset}: non_numeric_frame_name")
            temporal_costmaps.append(append_missing_temporal_map(temporal_costmaps, current_costmap, args))
            temporal_frames.append(f"missing+{offset}")
            temporal_valid.append(False)
            temporal_warped.append(False)
            continue

        # 根据未来帧编号构建未来帧的costmap、measurement和meta文件路径
        future_costmap_path = route_dir / costmap_folder / f"{future_name}.npy"
        future_measurement_path = route_dir / args.measurements_folder / f"{future_name}.json.gz"
        future_meta_path = route_dir / meta_folder / f"{future_name}.json.gz"
        # 如果未来帧的costmap或measurement文件不存在，记录缺失原因，并在temporal bundle中添加一个无效的占位costmap
        if not future_costmap_path.exists() or not future_measurement_path.exists():
            missing_reasons.append(
                f"offset={offset}, frame={future_name}: missing future costmap or measurement"
            )
            temporal_costmaps.append(append_missing_temporal_map(temporal_costmaps, current_costmap, args))
            temporal_frames.append(future_name)
            temporal_valid.append(False)
            temporal_warped.append(False)
            continue
        # 读取未来帧的costmap、measurement和meta信息，如果meta信息不存在则使用空字典
        future_costmap = load_costmap(future_costmap_path)
        future_measurement = load_json_gz(future_measurement_path)
        future_meta = load_json_gz(future_meta_path) if future_meta_path.exists() else {}

        # 关键步骤：将未来帧的costmap warp到当前帧的坐标系下，以便后续score_rollout可以直接采样比较
        warped = warp_costmap_to_current_frame(
            source_costmap=future_costmap,  # 未来帧的costmap数据
            source_meta=future_meta,  # 未来帧的meta信息
            source_measurement=future_measurement,  # 未来帧的measurement信息
            current_measurement=current_measurement,  # 当前帧的measurement信息
            target_shape=current_costmap.shape,  # 输出的costmap形状和当前帧的costmap一样
            target_ego_center=current_ego_center,  # 输出costmap中自车中心的像素坐标和当前帧一样
            target_meters_per_pixel=current_meters_per_pixel,  # 输出costmap的米/像素和当前帧一样
            args=args,
        )

        if warped is None:
            missing_reasons.append(
                f"offset={offset}, frame={future_name}: missing pos_global/theta for temporal warp"
            )
            temporal_costmaps.append(append_missing_temporal_map(temporal_costmaps, current_costmap, args))
            temporal_frames.append(future_name)
            temporal_valid.append(False)
            temporal_warped.append(False)
            continue

        # 融合当前帧costmap和warped的未来帧costmap
        combined = combine_current_and_future_costmap(
            current_costmap=current_costmap,
            future_costmap_in_current_frame=warped,
            args=args,
        )
        
        # Update cumulative temporal debug costmap.
        # This accumulates all warped future costmaps into one complete map.
        weighted_warped = warped.astype(np.float32) * float(args.future_costmap_weight)
        cumulative_mode = str(args.future_costmap_combine).lower()

        if cumulative_mode == "max":
            cumulative_temporal_costmap = np.maximum(
                cumulative_temporal_costmap,
                weighted_warped,
            )
        elif cumulative_mode == "add":
            cumulative_temporal_costmap = cumulative_temporal_costmap + weighted_warped
        elif cumulative_mode == "future_only":
            # For debug visualization, future_only means accumulate future risk only.
            if len(fused_future_frames) == 0:
                cumulative_temporal_costmap = weighted_warped.copy()
            else:
                cumulative_temporal_costmap = np.maximum(
                    cumulative_temporal_costmap,
                    weighted_warped,
                )
        else:
            raise ValueError(f"Unsupported future_costmap_combine: {args.future_costmap_combine}")

        if float(args.temporal_cost_clip_max) > 0.0:
            cumulative_temporal_costmap = np.clip(
                cumulative_temporal_costmap,
                0.0,
                float(args.temporal_cost_clip_max),
            ).astype(np.float32)

        fused_future_frames.append(future_name)

        temporal_costmaps.append(combined)
        temporal_frames.append(future_name)
        temporal_valid.append(True)
        temporal_warped.append(True)

    # Save one cumulative temporal debug costmap for the current frame.
    # This contains current costmap + all successfully warped future costmaps.
    if args.save_temporal_debug:
        save_cumulative_temporal_costmap_debug(
            route_dir=route_dir,
            frame_name=frame_name,
            cumulative_costmap=cumulative_temporal_costmap,
            current_ego_center=current_ego_center,
            fused_future_frames=fused_future_frames,
            args=args,
        )

    return {
        "costmaps": temporal_costmaps,
        "frames": temporal_frames,
        "valid": temporal_valid,
        "warped": temporal_warped,
        "missing_reasons": missing_reasons,
        "enabled": True,
    }


def temporal_index_for_dense_step(step_idx: int, num_temporal_maps: int, args) -> int:
    """
    Map a dense bicycle-model step to a temporal costmap index.

    The rollout is integrated at model_fps. Future costmaps are assumed to be
    stored at future_fps with frame stride args.future_frame_stride. With the
    default model_fps=20 and future_fps=4, every 5 integration steps correspond
    to one output waypoint / one future frame.
    """
    if num_temporal_maps <= 1:
        return 0

    t = float(step_idx + 1) / float(args.model_fps)
    raw = t * float(args.future_fps) / max(float(args.future_frame_stride), 1.0)
    mode = str(args.temporal_index_mode).lower()

    if mode == "floor":
        idx = int(math.floor(raw))
    elif mode == "ceil":
        idx = int(math.ceil(raw))
    elif mode == "round":
        idx = int(math.floor(raw + 0.5))
    else:
        raise ValueError(f"Unsupported temporal_index_mode: {args.temporal_index_mode}")

    return int(np.clip(idx, 0, num_temporal_maps - 1))


def score_rollout(
    rollout: Dict,
    reference_candidate: Dict,
    expert_reference_route: np.ndarray,
    costmap: np.ndarray,
    ego_center: List[float],
    meters_per_pixel: float,
    args,
    temporal_costmaps: Optional[List[np.ndarray]] = None,
    temporal_frames: Optional[List[str]] = None,
) -> Dict:
    """
    Score one candidate rollout.

    If temporal_costmaps is provided, dense rollout states are scored against
    the time-aligned costmap. temporal_costmaps[0] is the current frame;
    temporal_costmaps[k] is future frame k warped into the current ego-local
    coordinate system. This is how future true motion of surrounding actors is
    injected into planning.
    """
    if temporal_costmaps is None or len(temporal_costmaps) == 0:
        temporal_costmaps = [costmap]
    if temporal_frames is None or len(temporal_frames) != len(temporal_costmaps):
        temporal_frames = [str(i) for i in range(len(temporal_costmaps))]

    sampled_chunks = []
    valid_chunks = []
    temporal_indices_used = []
    temporal_cost_by_index: Dict[int, List[float]] = {}

    dense_xy = np.asarray(rollout["dense_xy"], dtype=np.float32)
    dense_yaw = np.asarray(rollout["dense_yaw"], dtype=np.float32)

    for step_i, (xy_i, yaw_i) in enumerate(zip(dense_xy, dense_yaw)):
        temporal_idx = temporal_index_for_dense_step(
            step_idx=step_i,
            num_temporal_maps=len(temporal_costmaps),
            args=args,
        )
        temporal_indices_used.append(int(temporal_idx))

        score_points = build_vehicle_footprint_points_single(
            xy=xy_i,
            yaw=float(yaw_i),
            args=args,
        )
        pixels = local_points_to_pixels(score_points, ego_center, meters_per_pixel)
        sampled_cost_i, valid_i = sample_cost_bilinear(
            temporal_costmaps[temporal_idx],
            pixels,
            out_of_bounds_cost=args.out_of_bounds_cost,
        )

        sampled_chunks.append(sampled_cost_i)
        valid_chunks.append(valid_i)
        temporal_cost_by_index.setdefault(int(temporal_idx), []).extend(
            sampled_cost_i.astype(float).tolist()
        )

    if len(sampled_chunks) == 0:
        sampled_cost = np.zeros((1,), dtype=np.float32)
        valid = np.zeros((1,), dtype=bool)
    else:
        sampled_cost = np.concatenate(sampled_chunks).astype(np.float32)
        valid = np.concatenate(valid_chunks).astype(bool)

    mean_cost = float(np.mean(sampled_cost))
    max_cost = float(np.max(sampled_cost))
    sum_cost = float(np.sum(sampled_cost))
    hard_ratio = float(np.mean(sampled_cost >= args.hard_cost_threshold))
    out_ratio = float(1.0 - np.mean(valid.astype(np.float32)))

    temporal_mean_costs = {
        str(k): float(np.mean(v)) if len(v) else 0.0
        for k, v in temporal_cost_by_index.items()
    }
    temporal_max_costs = {
        str(k): float(np.max(v)) if len(v) else 0.0
        for k, v in temporal_cost_by_index.items()
    }

    waypoints = rollout["waypoints"]
    dev = min_distance_to_polyline_points(waypoints, expert_reference_route)
    mean_route_deviation = float(np.mean(dev)) if len(dev) else 0.0
    max_route_deviation = float(np.max(dev)) if len(dev) else 0.0

    controls = rollout["dense_controls"]
    steer = controls[:, 0] if len(controls) else np.zeros(1, dtype=np.float32)
    acc = controls[:, 1] if len(controls) else np.zeros(1, dtype=np.float32)
    steer_rate = np.diff(steer) * float(args.model_fps) if len(steer) > 1 else np.zeros(1, dtype=np.float32)

    lat_accs = np.asarray([aux["lat_acc_mps2"] for aux in rollout["dense_aux"]], dtype=np.float32)
    yaw_rates = np.asarray([aux["yaw_rate_radps"] for aux in rollout["dense_aux"]], dtype=np.float32)

    max_abs_lat_acc = float(np.max(np.abs(lat_accs))) if len(lat_accs) else 0.0
    max_abs_yaw_rate = float(np.max(np.abs(yaw_rates))) if len(yaw_rates) else 0.0
    mean_abs_acc = float(np.mean(np.abs(acc)))
    mean_abs_steer = float(np.mean(np.abs(steer)))
    mean_abs_steer_rate = float(np.mean(np.abs(steer_rate)))

    progress_m = float(np.linalg.norm(waypoints[-1] - waypoints[0])) if len(waypoints) >= 2 else 0.0

    route_deviation_cost = args.route_deviation_weight * mean_route_deviation
    comfort_cost = (
        args.acc_weight * mean_abs_acc
        + args.steer_weight * mean_abs_steer
        + args.steer_rate_weight * mean_abs_steer_rate
        + args.lat_acc_weight * max_abs_lat_acc
        + args.yaw_rate_weight * max_abs_yaw_rate
    )

    behavior_name = str(reference_candidate.get("behavior_name", reference_candidate.get("reference_mode", "unknown")))
    behavior_id = int(reference_candidate.get("behavior_id", -1))
    behavior_active = bool(reference_candidate.get("behavior_active", True))
    behavior_prior_cost = 0.0

    if not behavior_active:
        behavior_prior_cost += float(args.inactive_behavior_penalty)

    if behavior_name in ["yield_stop", "emergency_brake"]:
        # Stop-like behaviors should not win purely because they minimize risk.
        # They are selected only when active or when all moving candidates are unsafe.
        behavior_prior_cost += float(args.stop_behavior_prior_cost)

    if behavior_name in ["left_nudge", "right_nudge"]:
        behavior_prior_cost += float(args.nudge_behavior_prior_cost)

    total_score = (
        args.mean_cost_weight * mean_cost
        + args.max_cost_weight * max_cost
        + args.hard_ratio_weight * hard_ratio
        + args.out_of_bounds_weight * out_ratio
        + route_deviation_cost
        + comfort_cost
        + behavior_prior_cost
    )

    allowed = True
    reasons = []

    if not behavior_active and not args.allow_inactive_behavior_selection:
        allowed = False
        reasons.append("behavior_inactive")

    if out_ratio > args.max_out_of_bounds_ratio:
        allowed = False
        reasons.append(f"out_of_bounds_ratio={out_ratio:.3f}>{args.max_out_of_bounds_ratio:.3f}")
    if hard_ratio > args.max_hard_ratio:
        allowed = False
        reasons.append(f"hard_ratio={hard_ratio:.3f}>{args.max_hard_ratio:.3f}")
    if max_abs_lat_acc > args.max_lateral_accel_mps2:
        allowed = False
        reasons.append(f"lat_acc={max_abs_lat_acc:.3f}>{args.max_lateral_accel_mps2:.3f}")
    if max_abs_yaw_rate > args.max_yaw_rate_radps:
        allowed = False
        reasons.append(f"yaw_rate={max_abs_yaw_rate:.3f}>{args.max_yaw_rate_radps:.3f}")
    if max_route_deviation > args.max_route_deviation_m:
        allowed = False
        reasons.append(f"route_deviation={max_route_deviation:.3f}>{args.max_route_deviation_m:.3f}")

    # Emergency brake is saved as a diagnostic/safety candidate, but by default it
    # should not become the selected risk-planned waypoint label. Otherwise many
    # turning or blocked frames may collapse to a straight braking trajectory.
    if behavior_name == "emergency_brake" and not args.allow_emergency_selection:
        allowed = False
        reasons.append("emergency_brake_not_selectable_by_default")

    # Optional: keep yield_stop unselectable unless requested.
    # if behavior_name == "yield_stop" and not args.allow_yield_selection and not bool(reference_candidate.get("behavior_active", True)):
    #     allowed = False
    #     reasons.append("yield_inactive_not_selectable")
    if behavior_name == "yield_stop" and not args.allow_yield_selection:
        allowed = False
        reasons.append("yield_stop_not_selectable_by_default")

    temporal_indices_unique = sorted(set(int(i) for i in temporal_indices_used))
    temporal_frames_used = [temporal_frames[i] for i in temporal_indices_unique if i < len(temporal_frames)]

    return {
        "behavior_id": behavior_id,
        "behavior_name": behavior_name,
        "behavior_active": behavior_active,
        "activation_reason": str(reference_candidate.get("activation_reason", "")),
        "reference_mode": reference_candidate["reference_mode"],
        "offset_m": float(reference_candidate["offset_m"]),
        "speed_mode": rollout["speed_mode"],
        "target_speed": float(rollout["target_speed"]),
        "score": float(total_score),
        "mean_cost": mean_cost,
        "max_cost": max_cost,
        "sum_cost": sum_cost,
        "hard_ratio": hard_ratio,
        "out_of_bounds_ratio": out_ratio,
        "mean_route_deviation": mean_route_deviation,
        "max_route_deviation": max_route_deviation,
        "comfort_cost": float(comfort_cost),
        "route_deviation_cost": float(route_deviation_cost),
        "behavior_prior_cost": float(behavior_prior_cost),
        "score_breakdown": {
            "mean_cost_term": float(args.mean_cost_weight * mean_cost),
            "max_cost_term": float(args.max_cost_weight * max_cost),
            "hard_ratio_term": float(args.hard_ratio_weight * hard_ratio),
            "out_of_bounds_term": float(args.out_of_bounds_weight * out_ratio),
            "route_deviation_cost": float(route_deviation_cost),
            "comfort_cost": float(comfort_cost),
            "behavior_prior_cost": float(behavior_prior_cost),
        },
        "mean_abs_acc": mean_abs_acc,
        "mean_abs_steer": mean_abs_steer,
        "mean_abs_steer_rate": mean_abs_steer_rate,
        "max_abs_lateral_accel": max_abs_lat_acc,
        "max_abs_yaw_rate": max_abs_yaw_rate,
        "progress_m": progress_m,
        "temporal_score_enabled": bool(len(temporal_costmaps) > 1),
        "temporal_indices_used": temporal_indices_unique,
        "temporal_frames_used": temporal_frames_used,
        "temporal_mean_costs": temporal_mean_costs,
        "temporal_max_costs": temporal_max_costs,
        "allowed": bool(allowed),
        "reasons": reasons,
    }

def select_best_rollout(scored_rollouts: List[Dict]) -> int:
    """
    Select the best dynamically feasible risk-planned rollout.

    Return:
        >=0: index of the best allowed candidate
        -1: no allowed candidate exists

    Important:
        If no candidate is allowed, do NOT select emergency_brake here.
        The caller should fall back to expert future waypoints and mark
        risk_label_valid=False.
    """
    allowed = [
        (i, r)
        for i, r in enumerate(scored_rollouts)
        if bool(r["info"].get("allowed", False))
    ]

    if len(allowed) > 0:
        best_i, _ = min(allowed, key=lambda ir: ir[1]["info"]["score"])
        return int(best_i)

    return -1


def build_valid_behavior_mask(scored_rollouts: List[Dict]) -> List[int]:
    mask = [0 for _ in BEHAVIOR_NAMES]
    for r in scored_rollouts:
        info = r.get("info", {})
        bid = int(info.get("behavior_id", -1))
        if 0 <= bid < len(mask) and bool(info.get("allowed", False)):
            mask[bid] = 1
    return mask


def estimate_yaws_and_speeds_from_waypoints(
    waypoints: np.ndarray,
    future_fps: float,
) -> Tuple[np.ndarray, np.ndarray]:
    """
    Estimate yaw and speed from expert future waypoints.

    This is only used for expert fallback debug/output.
    The fallback frame is not used as valid risk-planned supervision.
    """
    waypoints = np.asarray(waypoints, dtype=np.float32)

    if waypoints.ndim != 2 or waypoints.shape[1] < 2 or len(waypoints) == 0:
        return (
            np.zeros((0,), dtype=np.float32),
            np.zeros((0,), dtype=np.float32),
        )

    points = waypoints[:, :2]
    prev = np.concatenate(
        [np.zeros((1, 2), dtype=np.float32), points[:-1]],
        axis=0,
    )
    delta = points - prev

    yaws = np.arctan2(delta[:, 1], delta[:, 0]).astype(np.float32)
    speeds = (np.linalg.norm(delta, axis=1) * float(future_fps)).astype(np.float32)

    return yaws, speeds


def build_expert_fallback_rollout(
    expert_reference_route: np.ndarray,
    expert_future_waypoints: Optional[np.ndarray],
    args,
    fallback_reason: str = "no_allowed_behavior_candidate",
) -> Dict:
    """
    Build a selected fallback item from expert route + expert future waypoints.

    This fallback is only for output/debug stability. It must NOT be treated
    as valid risk-planned supervision, so:
        allowed=False
        risk_label_valid=False
        selector_label=-1
    """
    expert_reference_route = np.asarray(expert_reference_route, dtype=np.float32)

    if expert_future_waypoints is not None:
        waypoints = np.asarray(expert_future_waypoints, dtype=np.float32)
        if waypoints.ndim == 2 and waypoints.shape[1] >= 2 and len(waypoints) > 0:
            waypoints = waypoints[:, :2].astype(np.float32)
        else:
            waypoints = None
    else:
        waypoints = None

    # If future expert waypoints are unavailable near the end of a route,
    # fall back to route samples only for debug/output. This frame is still invalid
    # for risk-planned training because allowed=False.
    if waypoints is None or len(waypoints) == 0:
        query_s = np.arange(
            1,
            int(args.num_future_waypoints) + 1,
            dtype=np.float32,
        ) * float(args.reference_spacing_m)
        waypoints = sample_polyline_by_s(expert_reference_route, query_s)

    # Match expected number of future waypoints as much as possible.
    if len(waypoints) > int(args.num_future_waypoints):
        waypoints = waypoints[:int(args.num_future_waypoints)]
    elif len(waypoints) < int(args.num_future_waypoints):
        pad_num = int(args.num_future_waypoints) - len(waypoints)
        last = waypoints[-1:].copy()
        waypoints = np.concatenate(
            [waypoints, np.repeat(last, pad_num, axis=0)],
            axis=0,
        )

    yaws, speeds = estimate_yaws_and_speeds_from_waypoints(
        waypoints,
        future_fps=float(args.future_fps),
    )

    controls = np.zeros((len(waypoints), 2), dtype=np.float32)

    info = {
        "behavior_id": -1,
        "behavior_name": "expert_fallback",
        "behavior_active": False,
        "activation_reason": fallback_reason,
        "reference_mode": "expert_reference",
        "speed_mode": "expert_future",
        "offset_m": 0.0,

        # This fallback is intentionally invalid as risk-planned supervision.
        "allowed": False,
        "reasons": [fallback_reason],
        "fallback_to_expert": True,
        "fallback_reason": fallback_reason,

        # Keep numeric fields available for debug text and JSON consistency.
        "score": 1.0e9,
        "mean_cost": 0.0,
        "max_cost": 0.0,
        "hard_ratio": 0.0,
        "route_deviation_mean_m": 0.0,
        "route_deviation_max_m": 0.0,
        "mean_abs_acc": 0.0,
        "mean_abs_steer": 0.0,
        "mean_abs_steer_rate": 0.0,
        "max_abs_lateral_accel": 0.0,
        "max_abs_yaw_rate": 0.0,
        "progress_m": float(np.linalg.norm(waypoints[-1] - waypoints[0])) if len(waypoints) > 1 else 0.0,
        "temporal_score_enabled": False,
        "temporal_indices_used": [],
        "temporal_frames_used": [],
        "temporal_mean_costs": [],
        "temporal_max_costs": [],
    }

    rollout = {
        "waypoints": waypoints.astype(np.float32),
        "yaws": yaws.astype(np.float32),
        "speeds": speeds.astype(np.float32),
        "controls": controls,
        "dense_xy": waypoints.astype(np.float32),
        "dense_yaw": yaws.astype(np.float32),
        "dense_speed": speeds.astype(np.float32),
        "dense_controls": controls,
        "dense_aux": [],
        "target_speed": float(speeds[0]) if len(speeds) > 0 else 0.0,
        "speed_mode": "expert_future",
    }

    return {
        "info": info,
        "reference_route": expert_reference_route.astype(np.float32),
        "rollout": rollout,
    }


def safe_float(x, default: float = 0.0) -> float:
    try:
        if x is None:
            return float(default)
        v = float(x)
        if not np.isfinite(v):
            return float(default)
        return v
    except Exception:
        return float(default)

def mps_to_kmh(v: float) -> float:
    return float(v) * 3.6


def format_speed_en(v_mps: float) -> str:
    return f"{v_mps:.2f} m/s ({mps_to_kmh(v_mps):.1f} km/h)"


def format_speed_zh(v_mps: float) -> str:
    return f"{v_mps:.2f} m/s（约 {mps_to_kmh(v_mps):.1f} km/h）"


def estimate_path_length(points: Optional[np.ndarray]) -> float:
    if points is None:
        return 0.0
    pts = np.asarray(points, dtype=np.float32)
    if pts.ndim != 2 or pts.shape[0] < 2 or pts.shape[1] < 2:
        return 0.0
    return float(np.sum(np.linalg.norm(pts[1:, :2] - pts[:-1, :2], axis=1)))


def infer_expert_speed_stats(
    expert_future_waypoints: Optional[np.ndarray],
    future_fps: float,
) -> Dict:
    """
    Estimate expert future speed statistics from future ego waypoints.

    This is used only for language annotation. It does not affect planning.
    """
    if expert_future_waypoints is None:
        return {
            "available": False,
            "mean_speed_mps": 0.0,
            "max_speed_mps": 0.0,
            "path_length_m": 0.0,
        }

    pts = np.asarray(expert_future_waypoints, dtype=np.float32)
    if pts.ndim != 2 or pts.shape[0] < 2 or pts.shape[1] < 2:
        return {
            "available": False,
            "mean_speed_mps": 0.0,
            "max_speed_mps": 0.0,
            "path_length_m": estimate_path_length(pts),
        }

    prev = np.concatenate([np.zeros((1, 2), dtype=np.float32), pts[:-1, :2]], axis=0)
    delta = pts[:, :2] - prev
    speeds = np.linalg.norm(delta, axis=1) * float(future_fps)

    return {
        "available": True,
        "mean_speed_mps": float(np.mean(speeds)),
        "max_speed_mps": float(np.max(speeds)),
        "path_length_m": estimate_path_length(pts),
    }


